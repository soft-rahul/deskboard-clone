// Time 
function clock() {
    const h1 = document.querySelector('h1');
    const clock = new Date();
    let hour = clock.getHours();
    let minute = clock.getMinutes();
    if (minute < 10) {
        minute = `0${minute}`;
    }
    if (hour < 10) {
        hour = `0${hour}`;
    }
    h1.textContent = ` ${hour}:${minute} `;

}

setInterval(clock, 1000);


